    <h2>Titkos udvarok</h2>

    <h3>Márciusi kiállításunk</h3>

    <h5>Pesti bérházak szigorúan zárt kapui mögött titokzatos világok rejlenek. Minden udvarnak van története,
      múltja és jelene. A jövő bizonytalan. Az ott élők teszik élettel telivé a szigorúan szerkesztett oszlopok és
      lépcsőfordulók sokaságát. A teljesség igénye nélkül mutatok be párat ezekből a titkos udvarokból. Ezeket a
      fotógráfiákat egy Argentum 8×10 inches fakamerával készítem. Ha kiváncsi vagy a többire, a havonta megjelenő
      Budapest folyóirat minden számában található egy újabb épület, aminek a múltját Hídvégi Violetta mutatja be.
    </h5>


    <p>Sebestyén László évek óta fényképezi fekete/fehér képeken Pest elzárt udvarait. A fotóknak megkülönböztető
      figyelemre számot tartó technikai érdessége, hogy ezek egykor Klösz György által is használt fényképezőgép
      modern másolatával készültek. Ebben az esetben nem vonják el a figyelmet az udvarról a színek, hanem a maga
      egyszerű valóságában tárul fel egy „Titkos udvar”.</p>
    <p>A pesti házak egy évszázadot ölelnek fel: a XIX. század kezdetétől a XX. század elejéig ível sor, térben
      pedig a történeti városmagtól az egykori külvárosokban (a mai V.–IX., illetve a XIV. kerület) álló
      épületbelsők kerülnek bemutatásra. A korszak legjelentősebb építészei – Hild József, Pollack Mihály, Ybl
      Miklós – mellett a köztudatban kevéssé számot tartott szakemberek – Gottgeb Antal, Örömy József – alkotásai
